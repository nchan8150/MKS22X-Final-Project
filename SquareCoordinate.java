public class SquareCoordinate {

	private int x;
	private int y;

	public SquareCoordinate() {
		this(0, 0);
	}

	public SquareCoordinate(int x, int y) {
		x = 0;
		y = 0;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
			this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
			this.y = y;
	}
}
